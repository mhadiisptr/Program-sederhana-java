    //Membuat Tabel data
    public void tampilData()
    {
        String[] kolom = {"No Pendaftaran", "Nama Lengkap", "Jenis Kelamin", "Alamat Domisili     ", "Program Studi", "Nilai Akhir", "Sesi Perkuliahan"};
        Object [][] objData = new Object[this.listData.size()][7];
        
        int i = 0;
        for (Muhamad_Hadi_S_EntitasData n : this.listData)
        {
            String[] arrData = { String.valueOf(n.getnodaftar()), n.getnama(), n.getjk(), n.getalamat(), n.getprodi(), String.valueOf(n.getnilai()), 
                n.getsesi()};
            objData[i] = arrData;
            i++;
        }
        this.tabelModel = new DefaultTableModel(objData, kolom)
        {
            public boolean isCellEditable(int rowIndex, int colIndex)
                {
                    return false;
                }
        };
        this.tblData.setModel(this.tabelModel);       
    }
    //Isi Data
    public void isiData(int nodaftar, String nama, String jk, String alamat, String prodi, int nilai, String sesi)
    {
        Muhamad_Hadi_S_EntitasData ed = new Muhamad_Hadi_S_EntitasData(nodaftar, nama, jk, alamat, prodi, nilai, sesi);
        this.listData.add(ed);
    }
    
