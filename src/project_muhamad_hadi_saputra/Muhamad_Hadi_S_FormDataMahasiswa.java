    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // Simpan ke data tabel:
        if (this.tdaftar.getText().equals("")){
            this.tdaftar.setText("0");
        }
        if (this.tnama.getText().equals("")){
            this.tnama.setText("data kosong");
        }
        if (this.alamat.getText().equals("")){
            this.alamat.setText("Data kosong");
        }
         if (this.tprodi.getText().equals("")){
            this.tprodi.setText("Data Kosong");
        }
       
        if (this.tnilai.getText().equals("")){
            this.tnilai.setText("0");
        }
          if (this.tsesi.getText().equals("")){
            this.tsesi.setText("Data Kosong");
        }
        String jk = "";
        if (this.pria.isSelected()){
            jk = this.pria.getText();
        }else if (this.wanita.isSelected()){
            jk = this.wanita.getText();
        }
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
        
         isiData(Integer.parseInt(this.tdaftar.getText()), this.tnama.getText(), jk.intern(), this.alamat.getText(), this.tprodi.getText(), Integer.parseInt(this.tnilai.getText()), this.tsesi.getText());
    tampilData();
    resetInputan();
    }//GEN-LAST:event_simpanActionPerformed

    private void resetdataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetdataActionPerformed
        // reset input data:
    this.tnama.setText("");
        this.alamat.setText("");
        this.tdaftar.setText("");
        this.tprodi.setText("");
        this.tsesi.setText("");
        this.tnilai.setText("");
        this.tnama.requestFocus();
        this.jk.clearSelection();
        
        //hapus data tabel satu per satu
         int a = this.tblData.getSelectedRow();
    if (a != -1) {
      this.listData.remove(a);
    }
    tampilData();
   
    }//GEN-LAST:event_resetdataActionPerformed

    private void resetpenyimpananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetpenyimpananActionPerformed
        // Reset data tabel:
         DefaultTableModel mod = (DefaultTableModel)this.tblData.getModel();
    for (int i = mod.getRowCount() - 1; i >= 0; i--)
    {
      mod = (DefaultTableModel)this.tblData.getModel();
      mod.removeRow(i);
    }
    }//GEN-LAST:event_resetpenyimpananActionPerformed

