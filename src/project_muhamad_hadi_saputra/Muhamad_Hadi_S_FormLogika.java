    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(this.na.getText());
    int b = Integer.parseInt(this.nb.getText());
    int c = Integer.parseInt(this.nc.getText());
    
        boolean d = a > b;
        boolean e = a < b;
        boolean f = a == c;
        boolean g = b > c;
        boolean h = b < c;
        boolean i = a == b;

        boolean and1 = (d) && (e);
        boolean and2 = (f) && (g);
        boolean and3 = (h) && (i);

        boolean or1 = (d) || (e);
        boolean or2 = (f) || (g);
        boolean or3 = (h) || (i);

        boolean xor1 = d ^ e;
        boolean xor2 = f ^ g;
        boolean xor3 = h ^ i;

        boolean not1 = !d;
        boolean not2 = !f;
        boolean not3 = !h;

        this.tand1.setText(String.valueOf(and1));
        this.tand2.setText(String.valueOf(and2));
        this.tand3.setText(String.valueOf(and3));

        this.tor1.setText(String.valueOf(or1));
        this.tor2.setText(String.valueOf(or2));
        this.tor3.setText(String.valueOf(or3));

        this.txor1.setText(String.valueOf(xor1));
        this.txor2.setText(String.valueOf(xor2));
        this.txor3.setText(String.valueOf(xor2));

        this.tnot1.setText(String.valueOf(not1));
        this.tnot2.setText(String.valueOf(not2));
        this.tnot3.setText(String.valueOf(not3));
    }//GEN-LAST:event_jButton1ActionPerformed

