    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        this.db.koneksiDatabase();
    try
    {
      String query = "SELECT * FROM tbhadi_formuser where id_user = '" + this.cari.getText() + "'";
      java.sql.Statement st = this.db.getKoneksi().createStatement();
      java.sql.ResultSet rs = st.executeQuery(query);
      while (rs.next())
      {
        this.id_user.setText(rs.getString("id_user"));
        this.username.setText(rs.getString("username"));
        if (rs.getString("grup").equals("Admin")) {
          this.admin.setSelected(true);
        } else {
          this.user.setSelected(true);
        }
        this.password.setText(rs.getString("password"));
      }
    }
    catch (SQLException e)
    {
      JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan");
      getdata();
    }
  }
  
