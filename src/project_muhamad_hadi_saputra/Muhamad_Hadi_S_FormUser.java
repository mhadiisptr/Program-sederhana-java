    public void getdata()
  {
   
    DefaultTableModel tbm = new DefaultTableModel();
    
    
    tbm.addColumn("ID User");
    tbm.addColumn("User Name");
    tbm.addColumn("Group");
    tbm.addColumn("Password");
    this.tabel.setModel(tbm);
    this.db.koneksiDatabase();
    
    
    try
    {
      String query = "SELECT * FROM tbhadi_formuser";
      java.sql.Statement st = this.db.getKoneksi().createStatement();
      
      java.sql.ResultSet rs = st.executeQuery(query);
      while (rs.next())
      {
        tbm.addRow(new Object[] {rs
          .getString("id_user"), rs
          .getString("username"), rs
          .getString("grup"), rs
          .getString("password") });
        
        this.tabel.setModel(tbm);
      }
    }
    catch (SQLException e) {}
  }
  
