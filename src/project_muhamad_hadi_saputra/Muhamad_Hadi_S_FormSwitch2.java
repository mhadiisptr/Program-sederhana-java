        // TODO add your handling code here:
        char nilai = (char) this.Combopredikat.getSelectedIndex();
        switch (nilai)
        {
            case 1:
            this.tketerangan.setText("Pertahankan nilai anda!");
            break;
            case 2:
            this.tketerangan.setText("Terus menjadi lebih baik lagi");
            break;
            case 3:
            this.tketerangan.setText("Perbanyak belajar");
            break;
            case 4:
            this.tketerangan.setText("Jangan Keseringan bermain");
            break;
            case 5:
            this.tketerangan.setText("Kebanyakan bolos");
            default:
              

        }
